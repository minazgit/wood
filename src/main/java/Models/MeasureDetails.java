

package Models;

/**
 *
 * @author Minaz
 */
public class MeasureDetails {
    
   int pid;
   double length;
   double height;
   double thickness;
   double cubic_feet;
   int srno;

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getThickness() {
        return thickness;
    }

    public void setThickness(double thickness) {
        this.thickness = thickness;
    }

    public double getCubic_feet() {
        return cubic_feet;
    }

    public void setCubic_feet(double cubic_feet) {
        this.cubic_feet = cubic_feet;
    }

    public int getSrno() {
        return srno;
    }

    public void setSrno(int srno) {
        this.srno = srno;
    }
    
   
   
}
